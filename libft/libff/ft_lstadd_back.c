#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *node)
{
	t_list	*current;

	if (!node || !lst)
		return ;
	if (!*lst)
	{
		*lst = node;
		return ;
	}
	current = *lst;
	while (current->next)
		current = current->next;
	current->next = node;
}
